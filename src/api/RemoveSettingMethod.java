package api;

/**
 * Remove Setting Method
 *
 * 세터 제거하기
 */
public class RemoveSettingMethod {

    static class Old {
        private String name;
        private int age;

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }
    }

    static class New {
        private final String name;
        private final int age;

        public New(String name, int age) {
            this.name = name;
            this.age = age;
        }

        public String getName() {
            return name;
        }

        public int getAge() {
            return age;
        }
    }
}
